print("Vamos fazer uma conversão de temperatura de Cº para ºF");
c = float(input("Qual a temperatura da sua cidade no momento?: "));
f = (c * 1.8) + 32;
print(f'A temperatura de {c}ºC em Fahrenhet é de {f}ºF');
